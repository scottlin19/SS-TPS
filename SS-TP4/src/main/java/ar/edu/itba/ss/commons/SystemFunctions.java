package ar.edu.itba.ss.commons;

public abstract class SystemFunctions {

    public abstract double fx(Particle p, double t);
    public abstract double fy(Particle p, double t);

    public abstract double rx(Particle p, double t);
    public abstract double ry(Particle p, double t);

    public abstract double r1x(Particle p, double t);
    public abstract double r1y(Particle p, double t);

    public abstract double r2x(Particle p, double t);
    public abstract double r2y(Particle p, double t);

    public abstract double r3x(Particle p, double t);
    public abstract double r3y(Particle p, double t);

    public abstract double r4x(Particle p, double t);
    public abstract double r4y(Particle p, double t);

    public abstract double r5x(Particle p, double t);
    public abstract double r5y(Particle p, double t);

}
